import os

from google.genai import types

from config import MAX_CHARS

schema_get_file_content = types.FunctionDeclaration(
    name="get_file_content",
    description=f"Return the first {MAX_CHARS} characters of content from the file at the given file path",
    parameters=types.Schema(
        type=types.Type.OBJECT,
        properties={
            "file_path": types.Schema(
                type=types.Type.STRING,
                description="File path to get file content from, relative to the working directory (default is the working directory itself)",
            ),
        },
    ),
)


def get_file_content(working_directory, file_path):
    try:
        working_dir_abs = os.path.abspath(working_directory)
    except Exception:
        return f'Error: "{working_directory}" is not a directory'

    try:
        target_file = os.path.normpath(os.path.join(working_dir_abs, file_path))
    except Exception:
        return f'Error: File not found or is not a regular file: "{file_path}"'

    valid_target_dir = (
        os.path.commonpath([working_dir_abs, target_file]) == working_dir_abs
    )
    return_list = [f'Result for "{file_path}" file:']
    if not valid_target_dir:
        return_list.append(
            f'    Error: Cannot read "{file_path}" as it is outside the permitted working directory'
        )
    else:
        if not os.path.isfile(target_file):
            return_list.append(
                f'    Error: File not found or is not a regular file: "{file_path}"'
            )
        else:
            f = open(target_file)
            content = f.read(MAX_CHARS)
            if f.read(1):
                content += (
                    f'[...File "{file_path}" truncated at {MAX_CHARS} characters]'
                )
            return_list.append(content)

    return "\n".join(return_list)
